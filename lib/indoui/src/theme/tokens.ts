// Color palette names
export type ColorName = 
  | 'gray' | 'red' | 'orange' | 'yellow' | 'green' | 'teal' 
  | 'cyan' | 'blue' | 'indigo' | 'purple' | 'pink' | 'rose'
  | 'slate' | 'zinc' | 'neutral' | 'amber' | 'emerald';

export type ColorShade = 50 | 100 | 200 | 300 | 400 | 500 | 600 | 700 | 800 | 900 | 950;

// Helper to get color value
export const getColor = (color: ColorName, shade: ColorShade): string => {
  const colorObj = tokens.colors[color];
  if (typeof colorObj === 'object' && shade in colorObj) {
    return `hsl(${colorObj[shade]})`;
  }
  return '';
};
